package JavaBase;

public class VariableEx1 {

	public static void main(String[] args) {
		//타입 변수이름=값;
		int num1=0x36A;  // 16진수
		int num2=2;  // 10진수
		System.out.println(num1+num2);  // 10진수로 출력
		System.out.println(num1-num2);
		System.out.println(num1*num2);
		System.out.println(num1/num2);
		
	}

}
